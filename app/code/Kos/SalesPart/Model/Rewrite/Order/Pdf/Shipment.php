        StoreManagerInterface $storeManager,
  //      ResolverInterface $localeResolver,
        Database $fileStorageDatabase = null,
\Magento\Store\Model\App\Emulation $appEmulation,    
    array $data = []
    ) {
        $this->fileStorageDatabase = $fileStorageDatabase ?:
